def run(
    N=200_000,
    seed=2,
    regime="symmetric",
    beta_a=2.0,
    beta_b=2.0,
    sigma_d=0.7,
    sigma_i=0.7,
    bias_d=0.0,
    bias_i=0.0,
    corr=0.25,
    ell=0.5,
    w_ell=1.0,
    w_d=1.0,
    alpha=(1 + 5**0.5) / 2,
    extremize_gamma=1.5,
    hetero_strength_d=0.9,
    hetero_strength_i=1.2,
    tail_df=4,
    tail_scale_d=0.18,
    tail_scale_i=0.28,
):
    sim_kwargs = dict(
        N=N,
        seed=seed,
        beta_a=beta_a,
        beta_b=beta_b,
        sigma_d=sigma_d,
        sigma_i=sigma_i,
        bias_d=bias_d,
        bias_i=bias_i,
        corr=corr,
    )

    if regime == "asym_hetero_heavytail":
        sim_kwargs.update({
            "hetero_strength_d": hetero_strength_d,
            "hetero_strength_i": hetero_strength_i,
            "tail_df": tail_df,
            "tail_scale_d": tail_scale_d,
            "tail_scale_i": tail_scale_i,
        })

    p_true, y, xd, xi = simulate_forecasts(regime=regime, **sim_kwargs)

    methods = build_methods(
        xd, xi,
        alpha=alpha,
        ell=ell,
        w_ell=w_ell,
        w_d=w_d,
        extremize_gamma=extremize_gamma
    )
    df = evaluate_methods(y, methods)

    meta = {
        "N": N,
        "seed": seed,
        "regime": regime,
        "beta_a": beta_a,
        "beta_b": beta_b,
        "sigma_d": sigma_d,
        "sigma_i": sigma_i,
        "bias_d": bias_d,
        "bias_i": bias_i,
        "corr": corr,
        "extremize_gamma": extremize_gamma,
        "ell": ell,
        "w_ell": w_ell,
        "w_d": w_d,
        "alpha": float(alpha),
        "hetero_strength_d": hetero_strength_d if regime == "asym_hetero_heavytail" else np.nan,
        "hetero_strength_i": hetero_strength_i if regime == "asym_hetero_heavytail" else np.nan,
        "tail_df": tail_df if regime == "asym_hetero_heavytail" else np.nan,
        "tail_scale_d": tail_scale_d if regime == "asym_hetero_heavytail" else np.nan,
        "tail_scale_i": tail_scale_i if regime == "asym_hetero_heavytail" else np.nan,
    }
    return df, meta, p_true, y, xd, xi, methods


def alpha_sweep(alphas, regime="symmetric", **kwargs):
    sim_kwargs = dict(
        N=kwargs.get("N", 200_000),
        seed=kwargs.get("seed", 2),
        beta_a=kwargs.get("beta_a", 2.0),
        beta_b=kwargs.get("beta_b", 2.0),
        sigma_d=kwargs.get("sigma_d", 0.7),
        sigma_i=kwargs.get("sigma_i", 0.7),
        bias_d=kwargs.get("bias_d", 0.0),
        bias_i=kwargs.get("bias_i", 0.0),
        corr=kwargs.get("corr", 0.25),
    )

    if regime == "asym_hetero_heavytail":
        sim_kwargs.update({
            "hetero_strength_d": kwargs.get("hetero_strength_d", 0.9),
            "hetero_strength_i": kwargs.get("hetero_strength_i", 1.2),
            "tail_df": kwargs.get("tail_df", 4),
            "tail_scale_d": kwargs.get("tail_scale_d", 0.18),
            "tail_scale_i": kwargs.get("tail_scale_i", 0.28),
        })

    p_true, y, xd, xi = simulate_forecasts(
        regime=regime,
        **sim_kwargs
    )

    ell = kwargs.get("ell", 0.5)
    w_ell = kwargs.get("w_ell", 1.0)
    w_d = kwargs.get("w_d", 1.0)
    extremize_gamma = kwargs.get("extremize_gamma", 1.5)

    mean_pool = 0.5 * (xd + xi)
    logpool = log_pool([xd, xi])
    ext_mean = extremize(mean_pool, gamma=extremize_gamma)

    rows = []
    base_methods = {
        "data-only (xd)": xd,
        "intuition-only (xi)": xi,
        "mean pool": mean_pool,
        "log pool": logpool,
        "extremized mean": ext_mean,
    }
    base_df = evaluate_methods(y, base_methods)
    for _, r in base_df.iterrows():
        row = dict(r)
        row["regime"] = regime
        rows.append(row)

    for a in alphas:
        p = ara_anchor_mix(xd, xi, alpha=a, ell=ell, w_ell=w_ell, w_d=w_d)
        rows.append({
            "regime": regime,
            "method": f"ARA (alpha={a:.6g})",
            "brier": float(np.mean(brier(p, y))),
            "logloss": float(np.mean(logloss(p, y))),
        })

    return pd.DataFrame(rows).sort_values(["regime", "logloss", "brier"], ascending=[True, True, True])


def significance_table_for_regime(
    regime,
    N=200_000,
    seed=2,
    alpha=(1 + 5**0.5) / 2,
    n_boot=2000,
    **kwargs
):
    df, meta, p_true, y, xd, xi, methods = run(
        N=N,
        seed=seed,
        regime=regime,
        alpha=alpha,
        **kwargs
    )

    ara_name = f"ARA (alpha={alpha:.6g})"
    p_ara = methods[ara_name]
    p_mean = methods["mean pool"]
    p_log = methods["log pool"]

    rows = []

    for metric_name, loss_fn in [("brier", brier), ("logloss", logloss)]:
        loss_ara = loss_fn(p_ara, y)
        loss_mean = loss_fn(p_mean, y)
        loss_log = loss_fn(p_log, y)

        out_mean = compare_losses(loss_ara, loss_mean, n_boot=n_boot, seed=seed + 101)
        out_log = compare_losses(loss_ara, loss_log, n_boot=n_boot, seed=seed + 202)

        rows.append({
            "scenario": regime,
            "metric": metric_name,
            "comparison": "ARA vs mean pool",
            **out_mean
        })
        rows.append({
            "scenario": regime,
            "metric": metric_name,
            "comparison": "ARA vs log pool",
            **out_log
        })

    return pd.DataFrame(rows), df, meta


