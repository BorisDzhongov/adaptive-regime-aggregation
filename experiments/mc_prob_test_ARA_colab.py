def simulate_forecasts_symmetric(
    N=200_000,
    seed=0,
    beta_a=2.0,
    beta_b=2.0,
    sigma_d=0.7,
    sigma_i=0.7,
    bias_d=0.0,
    bias_i=0.0,
    corr=0.25,
):
    """
    Symmetric / homoskedastic correlated Gaussian regime.
    """
    rng = np.random.default_rng(seed)

    p_true = rng.beta(beta_a, beta_b, size=N)
    y = rng.binomial(1, p_true, size=N)

    cov = np.array([
        [sigma_d**2, corr * sigma_d * sigma_i],
        [corr * sigma_d * sigma_i, sigma_i**2]
    ], dtype=float)

    eps = rng.multivariate_normal(mean=[0.0, 0.0], cov=cov, size=N)
    z0 = logit(p_true)

    z_d = z0 + bias_d + eps[:, 0]
    z_i = z0 + bias_i + eps[:, 1]

    xd = sigmoid(z_d)
    xi = sigmoid(z_i)
    return p_true, y, xd, xi


def simulate_forecasts_asym_hetero_heavytail(
    N=200_000,
    seed=0,
    beta_a=2.0,
    beta_b=2.0,
    sigma_d=0.55,
    sigma_i=0.95,
    bias_d=-0.10,
    bias_i=0.14,
    corr=0.20,
    hetero_strength_d=0.9,
    hetero_strength_i=1.2,
    tail_df=4,
    tail_scale_d=0.18,
    tail_scale_i=0.28,
):
    """
    Asymmetric + heteroskedastic + heavy-tail regime.
    Noise variance depends on p_true and the two subsystems are intentionally asymmetric.
    """
    rng = np.random.default_rng(seed)

    p_true = rng.beta(beta_a, beta_b, size=N)
    y = rng.binomial(1, p_true, size=N)

    # Base correlated Gaussian shocks
    cov = np.array([
        [1.0, corr],
        [corr, 1.0]
    ], dtype=float)
    g = rng.multivariate_normal(mean=[0.0, 0.0], cov=cov, size=N)

    # Heteroskedastic scaling: larger variance near extremes
    # You can change this shape if desired, but this is explicit and interpretable.
    hetero_core = 1.0 + 4.0 * np.abs(p_true - 0.5)   # in [1, 3]
    scale_d = sigma_d * (hetero_core ** hetero_strength_d)
    scale_i = sigma_i * (hetero_core ** hetero_strength_i)

    # Heavy-tailed shocks from Student-t
    t_d = rng.standard_t(df=tail_df, size=N)
    t_i = rng.standard_t(df=tail_df, size=N)

    eps_d = scale_d * g[:, 0] + tail_scale_d * t_d
    eps_i = scale_i * g[:, 1] + tail_scale_i * t_i

    z0 = logit(p_true)
    z_d = z0 + bias_d + eps_d
    z_i = z0 + bias_i + eps_i

    xd = sigmoid(z_d)
    xi = sigmoid(z_i)
    return p_true, y, xd, xi


def simulate_forecasts(
    regime="symmetric",
    **kwargs
):
    if regime == "symmetric":
        return simulate_forecasts_symmetric(**kwargs)
    elif regime == "asym_hetero_heavytail":
        return simulate_forecasts_asym_hetero_heavytail(**kwargs)
    else:
        raise ValueError(f"Unknown regime: {regime}")


