def paired_ttest(diff):
    """
    Test mean(diff) == 0 for paired observations.
    """
    diff = np.asarray(diff, dtype=float)
    n = diff.size
    if SCIPY_AVAILABLE:
        zeros = np.zeros_like(diff)
        stat, p = ttest_rel(diff, zeros)
        return float(stat), float(p)

    mean_d = np.mean(diff)
    sd_d = np.std(diff, ddof=1)
    if sd_d == 0:
        return np.nan, np.nan
    t_stat = mean_d / (sd_d / np.sqrt(n))

    # fallback: no exact p-value without scipy
    return float(t_stat), np.nan


def wilcoxon_signed_rank(diff):
    diff = np.asarray(diff, dtype=float)
    diff = diff[np.isfinite(diff)]

    if SCIPY_AVAILABLE:
        try:
            stat, p = wilcoxon(diff, zero_method="wilcox", alternative="two-sided", mode="auto")
            return float(stat), float(p)
        except Exception:
            return np.nan, np.nan

    return np.nan, np.nan


def bootstrap_ci_mean(diff, n_boot=2000, alpha=0.05, seed=123):
    rng = np.random.default_rng(seed)
    diff = np.asarray(diff, dtype=float)
    n = diff.size

    idx = rng.integers(0, n, size=(n_boot, n))
    means = diff[idx].mean(axis=1)
    low = np.quantile(means, alpha / 2)
    high = np.quantile(means, 1 - alpha / 2)
    return float(low), float(high)


def compare_losses(loss_ara, loss_ref, n_boot=2000, seed=123):
    """
    diff = ARA - reference
    Negative diff => ARA better.
    """
    diff = np.asarray(loss_ara, dtype=float) - np.asarray(loss_ref, dtype=float)

    mean_diff = float(np.mean(diff))
    ci_low, ci_high = bootstrap_ci_mean(diff, n_boot=n_boot, seed=seed)
    t_stat, p_t = paired_ttest(diff)
    w_stat, p_w = wilcoxon_signed_rank(diff)

    return {
        "mean_diff": mean_diff,
        "boot_ci_low": ci_low,
        "boot_ci_high": ci_high,
        "t_stat": t_stat,
        "p_ttest": p_t,
        "w_stat": w_stat,
        "p_wilcoxon": p_w,
    }


