# -----------------------------
# Basic math helpers
# -----------------------------
def sigmoid(z):
    z = np.clip(z, -700, 700)
    return 1.0 / (1.0 + np.exp(-z))

def logit(p, eps=1e-12):
    p = np.clip(p, eps, 1 - eps)
    return np.log(p / (1 - p))

def clip01(p, eps=1e-12):
    return np.clip(p, eps, 1 - eps)

def brier(p, y):
    return (p - y) ** 2

def logloss(p, y):
    p = clip01(p)
    return -(y * np.log(p) + (1 - y) * np.log(1 - p))

def extremize(p, gamma=1.5):
    return sigmoid(gamma * logit(p))

def log_pool(p_list, w=None):
    p = np.vstack([clip01(p) for p in p_list])  # (m, N)
    if w is None:
        w = np.ones(p.shape[0])
    w = np.asarray(w, dtype=float)
    w = w / np.sum(w)
    log_p = np.sum(w[:, None] * np.log(p), axis=0)
    log_q = np.sum(w[:, None] * np.log(1 - p), axis=0)
    return clip01(np.exp(log_p) / (np.exp(log_p) + np.exp(log_q)))

def ara_anchor_mix(xd, xi, alpha, ell=0.5, w_ell=1.0, w_d=1.0):
    # xb = (w_ell*ell + w_d*xd + alpha*xi)/(w_ell + w_d + alpha)
    w_i = alpha
    return clip01((w_ell * ell + w_d * xd + w_i * xi) / (w_ell + w_d + w_i))


