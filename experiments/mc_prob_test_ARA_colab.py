def build_methods(xd, xi, alpha, ell=0.5, w_ell=1.0, w_d=1.0, extremize_gamma=1.5):
    mean_pool = 0.5 * (xd + xi)
    logpool = log_pool([xd, xi])
    ext_mean = extremize(mean_pool, gamma=extremize_gamma)
    ara = ara_anchor_mix(xd, xi, alpha=alpha, ell=ell, w_ell=w_ell, w_d=w_d)

    return {
        "data-only (xd)": xd,
        "intuition-only (xi)": xi,
        "mean pool": mean_pool,
        "log pool": logpool,
        "extremized mean": ext_mean,
        f"ARA (alpha={alpha:.6g})": ara,
    }


def evaluate_methods(y, methods):
    rows = []
    for name, p in methods.items():
        rows.append({
            "method": name,
            "brier": float(np.mean(brier(p, y))),
            "logloss": float(np.mean(logloss(p, y))),
        })
    return pd.DataFrame(rows).sort_values(["logloss", "brier"], ascending=[True, True])


