import numpy as np
import pandas as pd

phi = (1 + 5**0.5) / 2
L = 5.0

projects = ["A", "B", "C", "D"]

G = np.array([
    [8.5, 7.0, 9.0, 6.5],
    [7.5, 8.5, 6.5, 6.2],
    [6.0, 8.1, 5.5, 8.0],
    [6.5, 7.0, 5.0, 8.5],
    [6.0, 7.5, 8.5, 5.8],
    [7.0, 6.0, 8.0, 6.5],
    [6.5, 7.5, 6.0, 8.0],
    [5.5, 6.5, 4.5, 7.5],
    [5.5, 6.5, 4.0, 7.0],
    [6.0, 6.5, 5.0, 7.5],
    [7.0, 6.5, 8.5, 6.0],
], dtype=float)

I = np.array([
    [8.0, 6.5, 9.5, 6.0],
    [7.0, 9.0, 6.0, 7.0],
    [5.0, 6.5, 4.5, 7.5],
    [6.0, 7.5, 4.0, 8.0],
    [5.5, 7.0, 8.0, 6.0],
    [6.5, 5.0, 7.0, 6.0],
    [6.0, 7.0, 5.5, 7.5],
    [4.0, 5.5, 3.0, 7.0],
    [4.5, 6.0, 3.0, 6.5],
    [5.0, 6.0, 4.0, 7.0],
    [6.5, 6.0, 9.0, 5.5],
], dtype=float)

m, n = G.shape


def clip01to10(x):
    return np.clip(x, 0.0, 10.0)


def phi_current(Gv, Iv):
    return (L + Gv + phi * Iv) / (2.0 + phi)


def phi_regime(Gv, Iv):
    alphas = np.full(m, phi, dtype=float)
    alphas[7:10] = phi**2
    return (L + Gv + alphas[:, None, None] * Iv) / (2.0 + alphas[:, None, None])


def aggregate_scores(X):
    return X.mean(axis=0)


def winner_from_scores(S):
    return np.argmax(S, axis=0)


def deterministic_summary():
    S_wsm = aggregate_scores(0.5 * (G + I))
    S_phi = aggregate_scores(phi_current(G, I))
    S_reg = aggregate_scores(phi_regime(G[:, :, None], I[:, :, None])[:, :, 0])

    return pd.DataFrame({
        "WSM (mean)": S_wsm,
        "Φ current": S_phi,
        "Φ regime": S_reg
    }, index=projects)


def mc_test_A(
    N=180_000,
    seed=3,
    sigma_g=0.9,
    sigma_i=0.9,
    shock_prob=0.15,
    shock_delta=2.0
):
    rng = np.random.default_rng(seed)

    Gp = clip01to10(G[:, :, None] + rng.normal(0, sigma_g, size=(m, n, N)))
    Ip = clip01to10(I[:, :, None] + rng.normal(0, sigma_i, size=(m, n, N)))

    shock = rng.random(N) < shock_prob
    value_idx = np.arange(0, 5)

    if shock.any():
        Gp[np.ix_(value_idx, np.arange(n), np.where(shock)[0])] = clip01to10(
            Gp[np.ix_(value_idx, np.arange(n), np.where(shock)[0])] + shock_delta
        )
        Ip[np.ix_(value_idx, np.arange(n), np.where(shock)[0])] = clip01to10(
            Ip[np.ix_(value_idx, np.arange(n), np.where(shock)[0])] + shock_delta
        )

    base = int(np.argmax(aggregate_scores(0.5 * (G + I))))

    methods = {
        "WSM mean": 0.5 * (Gp + Ip),
        "Φ current α=φ": phi_current(Gp, Ip),
        "Φ regime (risk α=φ²)": phi_regime(Gp, Ip),
    }

    rows = []
    for name, X in methods.items():
        S = aggregate_scores(X)
        winners = winner_from_scores(S)
        freq = np.bincount(winners, minlength=n) / N
        flip_rate = 1.0 - freq[base]

        if shock.any():
            w_sh = winners[shock]
            freq_sh = np.bincount(w_sh, minlength=n) / w_sh.size
            flip_rate_sh = 1.0 - freq_sh[base]
        else:
            flip_rate_sh = np.nan

        rows.append({
            "method": name,
            "truth_winner": projects[base],
            "P(win A)": freq[0],
            "P(win B)": freq[1],
            "P(win C)": freq[2],
            "P(win D)": freq[3],
            "flip_rate vs truth": flip_rate,
            "flip_rate (shock-only)": flip_rate_sh,
        })

    return pd.DataFrame(rows).sort_values("flip_rate vs truth").reset_index(drop=True)


if __name__ == "__main__":
    print("Deterministic summary:")
    print(deterministic_summary().round(6))

    print("\nMonte Carlo Test A:")
    out = mc_test_A().round(6)
    print(out)

    out.to_csv("testA_results.csv", index=False)

