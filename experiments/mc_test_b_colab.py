import numpy as np
import pandas as pd

phi = (1 + 5**0.5) / 2
L = 5.0

projects = ["A", "B", "C", "D"]

# Given matrix G (11x4)
G = np.array([
    [8.5, 7.0, 9.0, 6.5],
    [7.5, 8.5, 6.5, 6.2],
    [6.0, 8.1, 5.5, 8.0],
    [6.5, 7.0, 5.0, 8.5],
    [6.0, 7.5, 8.5, 5.8],
    [7.0, 6.0, 8.0, 6.5],
    [6.5, 7.5, 6.0, 8.0],
    [5.5, 6.5, 4.5, 7.5],
    [5.5, 6.5, 4.0, 7.0],
    [6.0, 6.5, 5.0, 7.5],
    [7.0, 6.5, 8.5, 6.0],
], dtype=float)

# Intuition matrix I
I = np.array([
    [8.0, 6.5, 9.5, 6.0],
    [7.0, 9.0, 6.0, 7.0],
    [5.0, 6.5, 4.5, 7.5],
    [6.0, 7.5, 4.0, 8.0],
    [5.5, 7.0, 8.0, 6.0],
    [6.5, 5.0, 7.0, 6.0],
    [6.0, 7.0, 5.5, 7.5],
    [4.0, 5.5, 3.0, 7.0],
    [4.5, 6.0, 3.0, 6.5],
    [5.0, 6.0, 4.0, 7.0],
    [6.5, 6.0, 9.0, 5.5],
], dtype=float)

m, n = G.shape


def clip01to10(x):
    return np.clip(x, 0.0, 10.0)


def phi_current(Gv, Iv):
    return (L + Gv + phi * Iv) / (2.0 + phi)


def phi_regime(Gv, Iv):
    # risk criteria: 8..10 -> indices 7..9
    alphas = np.full(m, phi)
    alphas[7:10] = phi**2
    return (L + Gv + (alphas[:, None, None] * Iv)) / (2.0 + alphas[:, None, None])


def phi_adapted(Gv, Iv):
    # risk: φ² ; budget/meas: φ^-1 ; others: φ
    alphas = np.full(m, phi)
    alphas[7:10] = phi**2
    alphas[4:6] = phi**-1
    return (L + Gv + (alphas[:, None, None] * Iv)) / (2.0 + alphas[:, None, None])


def aggregate_scores(X):
    return X.mean(axis=0)  # shape: (n, N)


def winner_from_scores(S):
    return np.argmax(S, axis=0)


def mc_test_B(N=180_000, seed=4):
    rng = np.random.default_rng(seed)

    # criterion-wise reliability model
    sig_g = np.full(m, 0.9)
    sig_i = np.full(m, 0.9)

    # Risk (8..10): intuition more reliable
    sig_g[7:10] = 1.2
    sig_i[7:10] = 0.5

    # Budget + measurement (5..6): given/data more reliable
    sig_g[4:6] = 0.5
    sig_i[4:6] = 1.2

    # generate noisy matrices
    Gp = np.empty((m, n, N))
    Ip = np.empty((m, n, N))
    for k in range(m):
        Gp[k] = clip01to10(G[k, :, None] + rng.normal(0, sig_g[k], size=(n, N)))
        Ip[k] = clip01to10(I[k, :, None] + rng.normal(0, sig_i[k], size=(n, N)))

    methods = {
        "WSM mean": 0.5 * (Gp + Ip),
        "Φ current α=φ": phi_current(Gp, Ip),
        "Φ regime (risk α=φ²)": phi_regime(Gp, Ip),
        "Φ adapted (risk φ², budget/meas φ⁻¹)": phi_adapted(Gp, Ip),
    }

    # Oracle: precision-weighted fusion using known sigmas
    wg = 1.0 / (sig_g**2)
    wi = 1.0 / (sig_i**2)

    X_oracle = (
        wg[:, None, None] * Gp + wi[:, None, None] * Ip
    ) / (wg[:, None, None] + wi[:, None, None])

    oracle_scores = aggregate_scores(X_oracle)
    oracle_winner = winner_from_scores(oracle_scores)

    rows = []
    for name, X in methods.items():
        S = aggregate_scores(X)
        winners = winner_from_scores(S)

        accuracy = float(np.mean(winners == oracle_winner))

        chosen = oracle_scores[winners, np.arange(N)]
        best = oracle_scores[oracle_winner, np.arange(N)]
        regret = best - chosen

        rows.append({
            "method": name,
            "accuracy_vs_oracle": accuracy,
            "mean_regret": float(np.mean(regret)),
            "p95_regret": float(np.quantile(regret, 0.95)),
        })

    out = pd.DataFrame(rows).sort_values(
        ["mean_regret", "p95_regret", "accuracy_vs_oracle"],
        ascending=[True, True, False]
    )
    return out


if __name__ == "__main__":
    print("Monte Carlo Test B:")
    out = mc_test_B().round(6)
    print(out)
    out.to_csv("testB_results.csv", index=False)